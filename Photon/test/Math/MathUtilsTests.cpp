#include <Photon/Math/MathUtils.hpp>
#include <gtest/gtest.h>

TEST(MathUtilsTests, DegToRad)
{
    EXPECT_FLOAT_EQ(0.0f, Photon::degToRad(0.0f));
    EXPECT_FLOAT_EQ(1.5707963267948966192313216916398f, Photon::degToRad(90.0f));
    EXPECT_FLOAT_EQ(3.1415926535897932384626433832795f, Photon::degToRad(180.0f));
    EXPECT_FLOAT_EQ(4.7123889803846898576939650749193f, Photon::degToRad(270.0f));
    EXPECT_FLOAT_EQ(6.283185307179586476925286766559f, Photon::degToRad(360.0f));
    EXPECT_FLOAT_EQ(2.3561944901923449288469825374596f, Photon::degToRad(135.0f));
}